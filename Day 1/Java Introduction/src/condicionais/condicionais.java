package condicionais;

public class condicionais {
    public static void main(String[] args) {
        double idade = 22.50;
        if (idade > 22.60) {
            System.out.println("Maior");
        } else if (idade == 22.60) {
            System.out.println("Igual");
        } else {
            System.out.println("Menor");
        }
    }
}
